
def sum_number(a, b):
    """
    Функция сложения чисел
    :param: a - первое число, b - второе число
    :return: возвращается сумма чисел
    """
    return a + b


def subtraction_number(a, b):
    """
    Функция вычитания чисел
    :param: a - первое число, b - второе число
    :return: возвращается разность чисел
    """
    return a - b
